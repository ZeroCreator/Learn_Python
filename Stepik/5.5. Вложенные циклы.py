# Вложенные циклы.
# Формат вложенных циклов:
# for <переменная> in <объект>"
#    for <переменная> in <объект>:                  # вложенный цикл.
#       <действия>
#   <действия>

for i in range(3):
    for j in range(5):
        print('*', end='')
    print()                         # Перенос на новую строчку