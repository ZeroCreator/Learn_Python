# ДЕЛЕНИЕ НАЦЕЛО И ДЕЛЕНИЕ ПО ОСТАТКУ.

# 1. Деление нацело
# При делении //10 убирается последнее число. //100  - убираются последние два числа.
# и т.д.
print(8//10)
print(47//10)
print(68423//10)

# 2. Деление по остатку
# используется в регрессиях - отстаток всех элементов регрессии одинаковый.
# при %10 остаток равен последнему числу. %100 - последним двум числам. и т.д.
print(4563%100)
print(68432%1000)

print(5321//100)
print(5321%100)

print(5324243//1000)
print(5324243%1000)
