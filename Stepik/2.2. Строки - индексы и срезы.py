# СТРОКИ. ИНДЕКСЫ И СРЕЗЫ.
# Строка представляет собой упорядоченную коллекцию символов
# Строка не изменяемый объект

print('Индексы:')
s = 'hello world'
print(s)
print(s[6])

print('abcd'[1])

d = 'privet'
print(d[-1])
print(d[len(d)-1])

#
print('Срезы:') # Если необходимо взять не один индекс, а несколько.
print(s[2:6])
print(s[2:7])
print(s[6:9])

#
print('срез по самый конец:')
print(s[4:])

#
print('срез с начала:')
print(s[:6])

#
print('копия строки:')
print(s[:]) # Вся строка целиком

#
print('шаг:')
print(s[::2])
print(s[1::2])

#
print('взять всю строку наоборот:')
print(s[::-1])

print(s[2:8:3])

#
print('Как вывести измененное значение в строке:')
print(s[:4] + 'a' + s[5:])
print(s)

#
print('Как поменять значение в строке:')
s = s[:4] + 'a' + s[5:]
print(s)


