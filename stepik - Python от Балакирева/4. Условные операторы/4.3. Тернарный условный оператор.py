# (<r_1> if <c_1> else <r_2>) if <c_2> else <r_3>
# (<r_1> if <c_1> else <r_2>) if <c_2> else (<r_3> if <c_3> else <r_4>)
print("Максимальное из трех чисел")
a = 2
b = 3
c = -4

d = (a if a > c else c) if a > b else (b if b > c else c)
