# Тернарный условный оператор
# Синтаксис:
# <значение 1> if <условие> else <значение 2>
a = 2
b = 7

# не возвращает результат
if a > b:
    res = a
else:
    res = b


# тернарный оператор автоматически возвращает результат
res = a if a > b else b
print(res)

res = a + 2 if a > b else b - 5
print(res)

res = abs(a) if a > b else abs(b)
print(res)

res = print(a) if a > b else print(b)
print(res)

s = "python"
t = "upper"

res = s.upper() if t == "upper" else s
print(res)

a = 12
b = 7
s = [1, 2, a if a < b else b, 4, 5]
print(s)

