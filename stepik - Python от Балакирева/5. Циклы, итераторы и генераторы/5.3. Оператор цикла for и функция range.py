# Подвиг 9. Вводится список названий городов в одну строчку через пробел. Перебрать все эти названия с помощью цикла
# for и определить, начинается ли название следующего города на последнюю букву предыдущего города в списке.
# Если последними встречаются буквы 'ь', 'ъ', 'ы', то берется следующая с конца буква. Вывести на экран ДА,
# если последовательность удовлетворяет этому правилу и НЕТ - в противном случае.
lst_in = input().lower().split()
out = "ДА"
g = ["ь", "ъ", "ы"]

for i in range(len(lst_in) - 1):
    if lst_in[i][-1] in g:
        if lst_in[i+1][0] != lst_in[i][-2]:
            out = "НЕТ"
            break

    else:
        if lst_in[i+1][0] != lst_in[i][-1]:
            out = "НЕТ"
            break
print(out)

