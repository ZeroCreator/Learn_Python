# Треугольник Паскаля
# правило формирования новой строки: row = [1]*[i+1]
# правило подсчета средних значений: row[j] = P[i-1][j-1] + P[j-1][j]
# условия крайних значений: j == 0 or j == i
N = 7 # глубина треугольника Паскаля
P = []

for i in range(N):
    row = [1] * (i + 1)
    for j in range(i + 1):
        if j != 0 and j != i:
            row[j] = P[i-1][j-1] + P[i-1][j]

    P.append(row)

for r in P:
    print(r)
